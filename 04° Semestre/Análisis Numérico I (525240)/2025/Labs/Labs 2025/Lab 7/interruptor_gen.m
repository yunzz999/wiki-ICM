s1=0.1;
s2=0.2;
s3=0.3;
s4=0.4;

f1=@(t,g) s1-1.51*g+3.03*(g^2/(1+g^2));
f2=@(t,g) s2-1.51*g+3.03*(g^2/(1+g^2));
f3=@(t,g) s3-1.51*g+3.03*(g^2/(1+g^2));
f4=@(t,g) s4-1.51*g+3.03*(g^2/(1+g^2));

[t1,y1]=ode45(f1,[0 100],0);
[t2,y2]=ode45(f2,[0 100],0);
[t3,y3]=ode45(f3,[0 100],0);
[t4,y4]=ode45(f4,[0 100],0);

plot(t1,y1,"r--")
hold on
plot(t2,y2,"m--")
hold on
plot(t3,y3,"b--")
hold on
plot(t4,y4,"g--")
grid on
legend("s=0.1","s=0.2","s=0.3","s=0.4")

